# Function for finding the area of a rectangle or square
def area_rec(a,b):
    c = a*b
    return c

# Function for finding the area of a triangle
def area_tri(d,e):
    f = (d*e)/2
    return f

# Function for finding the area of a circle
def area_cir(r):
    o = 3.1415926*(r**2)
    return o
